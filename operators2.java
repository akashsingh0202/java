public class operators2{
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x+=3;
        System.out.println(x);
        x-=3;
        System.out.println(x);
        x*=3;
        System.out.println(x);
        x/=3;
        System.out.println(x);
        x%=3;
        System.out.println(x);
        x&=3;
        System.out.println(x);
        x|=3;
        System.out.println(x);
        x^=3;
        System.out.println(x);
        x>>=3;
        System.out.println(x);
        x<<=3;
        System.out.println(x);


    }
    
}
