//comparison operator
public class operators3 {
    public static void main(String[] args) {
    int x =10;
    int y=23;
    System.out.println(x==y);
    System.out.println(x!=y);
    System.out.println(x>y);
    System.out.println(x<y);
    System.out.println(x>=y);
    System.out.println(x<=y);
    }
}
