//Arithmetic operators
public class operators1 {
    public static void main(String[] args) {
        int a =10;
        int b=20;
        int c=a+b;
        int d=b-a;
        int e=a*b;
        int f=b/a;
        int g=b%a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        int h=10;
        ++h;
        System.out.println(h);
        --h;
        System.out.println(h);
        h++;
        System.out.println(h);
        h--;
        System.out.println(h);

    }
    
}
